public class Accessers {
    private String username;
    private String userport;
    private String userIPAd;

    public void setUsername(String s) {
        this.username = s;
    }

    public void setUserport(String s) {
        this.userport = s;
    }

    public void setUserIPAd(String s) {
        this.userIPAd = s;
    }

    public String getUsername(String s) {
        return this.username;
    }

    public String getUserport(String s) {
        return this.userport;
    }

    public String getUserIPAd(String s) {
        return this.userIPAd;
    }
}
